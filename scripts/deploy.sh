#!/usr/bin/env bash
# =============================================================================
# Timin 云服务器部署脚本
# 用法: bash scripts/deploy.sh
# =============================================================================

set -euo pipefail  # 遇到错误立即退出，未定义变量报错，管道中任一命令失败则整体失败

echo "=============================================="
echo "  Timin 部署开始 — $(date '+%Y-%m-%d %H:%M:%S')"
echo "=============================================="

# ---- 切换到项目根目录 ------------------------------------------------
cd "$(dirname "$0")/.."
echo "[1/5] 工作目录: $(pwd)"

# ---- 拉取最新代码 ----------------------------------------------------
echo "[2/5] 拉取最新代码..."
git pull origin master

# ---- 安装生产依赖 ----------------------------------------------------
echo "[3/5] 安装生产依赖..."
npm ci --production

# ---- 以服务器模式构建 ------------------------------------------------
echo "[4/5] 构建应用（服务器模式）..."
DEPLOY_TARGET=server npm run build

# ---- 平滑重载 PM2 ----------------------------------------------------
echo "[5/5] 重载 PM2 进程..."
pm2 reload ecosystem.config.cjs || pm2 start ecosystem.config.cjs

echo "=============================================="
echo "  部署完成 — $(date '+%Y-%m-%d %H:%M:%S')"
echo "=============================================="
